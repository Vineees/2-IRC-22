# 1) Faça um Programa que leia 5 números inteiros, mostre a soma e a multiplicação desses numeros

#Variaveis
num1 = int(input('Primeiro Número:'))
num2 = int(input('Segundo Número:'))
num3 = int(input('Terceiro Número:'))
num4 = int(input('Quarto Número:'))
num5 = int(input('Quinto Número:'))

#Resposta
print ('\nNumeros escolhidos:'
        '\nNum1:', num1,
        '\nNum2:', num2,
        '\nNum3:', num3,
        '\nNum4:', num4,
        '\nNum4:', num5)

print ('\nSoma dos Numeros:', num1 + num2 + num3 + num4 + num5)
print ('Multiplicação', num1 + num2 + num3 + num4 + num5)
